import React from 'react';
import { IconButton } from '@material-ui/core';
import { makeStyles } from '@material-ui/core/styles';
import {
  PlusOne as PlusOneIcon,
  ExposureNeg1 as MinusOneIcon,
  Delete as DeleteIcon
} from '@material-ui/icons';

const useStyles = makeStyles(theme => ({
  buttons: {
    display: 'flex',
    flexFlow: 'row nowrap',
    justifyContent: 'center',
    minWidth: '72px'
  }
}));

function UnitActions({
  incrementUnit = undefined,
  decrementUnit,
  isKillPointMode,
  handleAddKillPoints,
  handleRemoveKillPoints
}) {
  const classes = useStyles();
  const fontSize = 26;
  if (isKillPointMode) {
    return (
      <div className={classes.buttons}>
      <React.Fragment>
        <IconButton
          size="small"
          onClick={handleRemoveKillPoints}
          style={{ marginLeft: 2, marginRight: 1 }}
        >
          <MinusOneIcon style={{ fontSize }} />
        </IconButton>
        <IconButton
          size="small"
          onClick={handleAddKillPoints}
          style={{ marginLeft: 1, marginRight: 2 }}
        >
          <PlusOneIcon style={{ fontSize }} />
        </IconButton>
      </React.Fragment>
    </div>
    );
  } else {
    return (
      <div className={classes.buttons}>
        {incrementUnit ? (
          <React.Fragment>
            <IconButton
              size="small"
              onClick={decrementUnit}
              style={{ marginLeft: 2, marginRight: 1 }}
            >
              <MinusOneIcon style={{ fontSize }} />
            </IconButton>
            <IconButton
              size="small"
              onClick={incrementUnit}
              style={{ marginLeft: 1, marginRight: 2 }}
            >
              <PlusOneIcon style={{ fontSize }} />
            </IconButton>
          </React.Fragment>
        ) : (
          <IconButton
            size="small"
            onClick={decrementUnit}
          >
            <DeleteIcon style={{ fontSize }} />
          </IconButton>
        )}
      </div>
    );
  }
};

export default UnitActions;
